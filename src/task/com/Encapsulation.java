package task.com;

public class Encapsulation {
	
	private int empoyeeId;
	private double empoyeesalary;
	private long empoyeeMobileno;
	private String empoyeeName;
	
	public int getEmpoyeeId() {
		return empoyeeId;
	}
	public void setEmpoyeeId(int empoyeeId) {
		this.empoyeeId = empoyeeId;
	}
	public double getEmpoyeesalary() {
		return empoyeesalary;
	}
	public void setEmpoyeesalary(double empoyeesalary) {
		this.empoyeesalary = empoyeesalary;
	}
	public long getEmpoyeeMobileno() {
		return empoyeeMobileno;
	}
	public void setEmpoyeeMobileno(long empoyeeMobileno) {
		this.empoyeeMobileno = empoyeeMobileno;
	}
	public String getEmpoyeeName() {
		return empoyeeName;
	}
	public void setEmpoyeeName(String empoyeeName) {
		this.empoyeeName = empoyeeName;
	}
	

}
